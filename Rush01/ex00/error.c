#include <unistd.h>

int		errordetect(char *str)
{
	int i;
	int flag;

	i = 0;
	flag = 0;
	while (str[i] != '\0' && flag == 0)
	{
		(i % 2 == 0 && !(str[i] >= '1' && str[i] <= '4')) && (flag++);
		(i % 2 == 1 && str[i] != 32) && (flag++);
		i++;
	}
	(i != 31) && (flag++);
	i = 0;
	while (str[i] != '\0' && flag == 0)
	{
		if (i % 2 == 0 && ((i >= 0 && i <= 7) || (i >= 16 && i <= 23)))
		{
			(str[i] == '4' && str[i + 8] == '4') && (flag++);
			(str[i] == '1' && str[i + 8] == '1') && (flag++);
		}
		i++;
	}
	(flag != 0) && (write(1, "Error\n", 6) && flag == 1);
	return (flag);
}
