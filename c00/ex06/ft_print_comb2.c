#include <unistd.h>

void	ft_print_comb2(void)
{
	int a;
	int b;
	int e;

	a = (0 - 1);
	while (a++ < 98)
	{
		b = a;
		while (b++ < 99)
		{
			e = ((a / 10) + '0');
			write(1, &e, 1);
			e = ((a % 10) + '0');
			write(1, &e, 1);
			write(1, " ", 1);
			e = ((b / 10) + '0');
			write(1, &e, 1);
			e = ((b % 10) + '0');
			write(1, &e, 1);
			if (a != 98)
			{
				write(1, ", ", 2);
			}
		}
	}
}
