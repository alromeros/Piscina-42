#include <stdio.h>

int		ft_sqrt(int nb)
{
	int i;

	i = 1;
	if (nb == 1)
	{
		return (1);
	}
	while (i * i != nb && i < 46341)
	{
		i++;
		if (i * i == nb)
		{
			return (i);
		}
	}
	return (0);
}
